package PieToast;

public class forloop {
	public static void main(String[] args) {
		for(int i=50; i>1; i--) {
			System.out.print("");
			for(int o=0; o<i+1; o++) {
				System.out.print("* ");
				
			}
			System.out.println(" ");
		}
		for(int p=0; p<50; p++) {
			System.out.print("");
			for(int a=0; a<p+1; a++) {
				System.out.print("* ");
				
			}
			System.out.println("");
		}
	}
}
